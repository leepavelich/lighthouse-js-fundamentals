const whereCanIPark = function (spots, vehicle) {
  for (let row = 0; row < spots.length; row++) {
    for (let col = 0; col < spots[row].length; col++) {
      let spot = spots[row][col]
      
      if (vehicle === 'regular') {
        if (spot === 'R') return [col, row]
      }

      if (vehicle === 'small') {
        if (spot === 'R' || spot === 'S') return [col, row]
      }

      if (vehicle === 'motorcycle') {
        if (spot === 'R' || spot === 'S' || spot === 'M') return [col, row]
      }
    }
  }

  return false
};

console.log(whereCanIPark(
  [
    // COLUMNS ARE X
    // 0    1    2    3    4    5
    ['s', 's', 's', 'S', 'R', 'M'], // 0 row ARE Y
    ['s', 'M', 's', 'S', 'r', 'M'], // 1
    ['s', 'M', 's', 'S', 'r', 'm'], // 2
    ['S', 'r', 's', 'm', 'r', 'M'], // 3
    ['S', 'r', 's', 'm', 'r', 'M'], // 4
    ['S', 'r', 'S', 'M', 'M', 'S']  // 5
  ],
  'regular'
));

console.log(whereCanIPark(
  [
    ['M', 'M', 'M', 'M'],
    ['M', 's', 'M', 'M'],
    ['M', 'M', 'M', 'M'],
    ['M', 'M', 'r', 'M']
  ],
  'small'
));

